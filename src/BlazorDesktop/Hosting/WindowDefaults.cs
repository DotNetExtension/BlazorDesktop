namespace BlazorDesktop.Hosting;

/// <summary>
/// Contains a set of constants representing configuration keys.
/// </summary>
public static class WindowDefaults
{
    /// <summary>
    /// The configuration key associated with the title.
    /// </summary>
    public static readonly string Title = "window:title";

    /// <summary>
    /// The configuration key associated with the height.
    /// </summary>
    public static readonly string Height = "window:height";

    /// <summary>
    /// The configuration key associated with the width.
    /// </summary>
    public static readonly string Width = "window:width";

    /// <summary>
    /// The configuration key associated with the frame.
    /// </summary>
    public static readonly string Frame = "window:frame";

    /// <summary>
    /// The configuration key associated with resizability.
    /// </summary>
    public static readonly string Resizable = "window:resizeable";

    /// <summary>
    /// The configuration key associated with the icon.
    /// </summary>
    public static readonly string Icon = "window:icon";
}
